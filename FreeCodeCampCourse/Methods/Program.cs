/*
 * Parameter: Variable that is being used inside the method.
 * Argument: Value that is passed when the method is called.
 * Overloaded methods examples.
 */

int number = 7;
string text = "Seven";

Console.WriteLine(number);      //Uses a method signature that receives an int argument.
Console.WriteLine();            //Uses a method signature that does not receive any arguments.
Console.WriteLine(text);        //Uses a method signature that receives a string argument.